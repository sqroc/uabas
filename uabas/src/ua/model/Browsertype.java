package ua.model;

public class Browsertype {
	private int ie;
	private int Safari;
	private int Opera;
	private int Firefox;
	private int Chrome;
	private int other;

	public int getIe() {
		return ie;
	}

	public void setIe(int ie) {
		this.ie = ie;
	}

	public int getSafari() {
		return Safari;
	}

	public void setSafari(int safari) {
		Safari = safari;
	}

	public int getOpera() {
		return Opera;
	}

	public void setOpera(int opera) {
		Opera = opera;
	}

	public int getFirefox() {
		return Firefox;
	}

	public void setFirefox(int firefox) {
		Firefox = firefox;
	}

	public int getChrome() {
		return Chrome;
	}

	public void setChrome(int chrome) {
		Chrome = chrome;
	}

	public int getOther() {
		return other;
	}

	public void setOther(int other) {
		this.other = other;
	}

}
